                <h1>User Experience</h1>

<h2 class="headline">PROBLEM</h2>
                    <p>I was not hired to do any UX work, so I only did a quick analysis. I did not do any extensive wire framing. My Fast PC is a PC repair and cleaning service. Their site had some usability issues with navigation and other elements on the page. I performed a heuristic analysis to identify these issues. Here is a screen shot of the original site:</p>
                    
                    <div class="row image-block">
                        <div class="col-sm-3 hidden-xs"></div>
                        <div class="col-xs-12 col-sm-6"><a href="images/myfastpc/mfpc-old.jpg" data-lightbox="mfpcold" title="Click for bigger image"><img src="images/myfastpc/mfpc-old.jpg" title="Old My Fast PC web site" alrt="Screenshot of old web site"></a></div>
                        <div class="col-sm-3 hidden-xs"></div>
                    
                    </div>
                    
                    <p>My Fast PC was assessed according to Jacob Nielson’s 10 usability heuristics. Below is a list of problems, the heuristics violated, and the severity. The next section contains a sample of my redesign intended to fix these problems as completely as possible.</p>
                    
                    <h3>Severity Rating Scale</h3>
                    
                    <ul class="list-none">
                        <li>0 - Not really a usability problem</li>
                        <li>1 - Cosmetic problem</li>
                        <li>2 - Minor usability problem</li>
                        <li>3 - Major usability problem; important to fix</li>
                        <li>4 - Usability disaster, imperative to fix</li>
                    </ul>
                    
                     <h3>Heuristics Evaluated</h3>
                    
                    <ul class="list-none">
                        
                        <li>1 - Visibility of system status</li>
                        <li>2 - Match between system and the real world</li>
                        <li>3 - User control and freedom</li>
                        <li>4 - Consistency and standards</li>
                        <li>5 - Error prevention</li>
                        <li>6 - Recognition rather than recall</li>
                        <li>7 - Flexibility and efficiency of use<li>
                        <li>8 - Aesthetic and minimalist design</li>
                        <li>9 - Help users recognize, diagnose, and recover from errors</li>
                        <li>10 - Help and documentation</li>
                    </ul>
                    
                   
                    
                    <h3>Issues</h3>
                    
                    <div class="row">
                        <div class="col-sm-4">
                        <p>Issue: Ambiguous Navigation Terms<br>Heuristics: 2, 5, 8<br>Severity: 3<br><br>Details:<br>There are four navigation links that are similar in meaning. “Get help now,” “Services,” “What we do,” and “Connect” are all viable options to choose when in need of a computer diagnosis. However, each leads to a different page.<br><br>Solution:<br>Clarify the purpose of the links with more specific terms. There is a mismatch between the link name and the header on the page to which it leads. I suggest “Phone support,” “Our services,” “How we do it,” and “Diagnose” respectively.</p>
                        </div>
                        <div class="col-sm-4">
                         <p>Issue: Icons Below Image Not Clickable<br>Heuristics: 2, 4<br>Severity: 2<br><br>Details:<br>The icons appear as though they they can be clicked, when in reality they are not linked to anything. The icons are informational only.<br><br>Solution:<br>The icons should link to the diagnosis page. This gives the user another avenue by which to reach their goal. Another solution is to remove the icons and free up some space on the main page to reduce clutter.</p>
                        </div>
                        <div class="col-sm-4">
                        <p>Issue: No Download Instructions Available<br>Heuristics: 1, 10<br>Severity: 3<br><br>Details:<br>When the user selects “Free Diagnosis” they are taken to a page to download the software. There are no download instructions, no download status and no software installation guides. The user must be experienced with downloading software to use this feature.<br><br>Solution:<br>The system should display instructions on how to recognize that a download is ocurring, how to set up the software, etc.The software requires only to be opened to run, with no setup necessary. This should be made apparent.</p>
                        </div>
                       
                    
                    </div>
                    
                    <p class="bump">I probably could have thrown in another issue of the site being ugly and cluttered, but the whole point of the job was to fix the design, so this was a given. Another thing to mention was that the person commissioning the new design was micro-managing the process somewhat. He had very specific ideas about what he wanted, gave me mood boards, etc. So I was not designing the ideal blue-sky design. I was giving him what he wanted. In the end my guess was he wasn't satisfied with the result of his own criterion. Here are some screen shots of design variations I came up with according to his instruction:</p>
                    
